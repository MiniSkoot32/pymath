def add(*args):
        res = sum(args)
        return res

def subtract(num1, num2):
        res = num1 - num2
        return res

def multiply(num1, num2):
        res = num1 * num2
        return res

def divide(num1, num2):
        res = num1 / num2
        return res

def floor_divide(num1, num2):
        res = num1 // num2
        return res
